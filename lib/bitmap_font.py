FONT_W = 9    # glyph width in bits
FONT_H = 13   # glyph height in rows

SUPPORTED_CHARS = tuple(FONT_9x13.keys())

__all__ = [
    "FONT_9x13",
    "FONT_H",
    "FONT_W",
    "SUPPORTED_CHARS",
    "get_glyph",
    "glyph_to_text",
]


def get_glyph(char: str) -> list[int]:
    """Return a 9x13 glyph bitmap, falling back to space for unknown characters."""
    if not char:
        return FONT_9x13[" "]
    return FONT_9x13.get(char.upper()[0], FONT_9x13[" "])


def glyph_to_text(char: str, on: str = "#", off: str = ".") -> str:
    """Render one glyph as text rows for quick inspection or tests."""
    glyph = get_glyph(char)
    rows = []
    for row_bits in glyph:
        rows.append("".join(on if row_bits & (1 << (FONT_W - 1 - col)) else off for col in range(FONT_W)))
    return "\n".join(rows)
